public class PBO210116374Latihan15Logika {

    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        System.out.println(" a && b \t= " + (a && b));
        System.out.println(" a || b \t= " + (a && b));
        System.out.println(" !a \t\t= " + !a);
        System.out.println(" !(a && b) \t= " + (a && b));
    }
    
}
